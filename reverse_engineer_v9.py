def csv_to_k(v):
    """元 → 千元"""
    return round(v / 1000)

def csv_to_w(v):
    """元 → 万元(保留整数)"""
    return round(v / 10000)

def csv_to_w1(v):
    """元 → 万元(保留1位小数)"""
    return round(v / 10000, 1)

