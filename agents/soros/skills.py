SKILLS = {
    "fx_market": {
        "name": "外汇市场分析",
        "instruction": FX_MARKET_INSTRUCTION,
        "tools": ["get_spot_rates", "get_vol_data", "get_news", "get_economic_calendar"]
    },
    "reflexivity": {
        "name": "反射性理论分析",
        "instruction": REFLEXIVITY_INSTRUCTION,
        "tools": ["analyze_reflexivity", "get_sentiment"]
    },
    "trade_history": {
        "name": "经典交易案例",
        "instruction": TRADE_HISTORY_INSTRUCTION,
        "tools": []
    }
}


def get_skill_instruction(skill_name: str) -> str:
    """获取技能指令"""
    skill = SKILLS.get(skill_name)
    if skill:
        return skill["instruction"]
    return ""


def get_all_instructions() -> str:
    """获取所有技能的合并指令"""
    parts = ["# 可用技能"]
    
    for skill_id, skill in SKILLS.items():
        parts.append(f"\n## {skill['name']} ({skill_id})")
        parts.append(skill["instruction"])
    
    return "\n".join(parts)


def get_tools_for_skill(skill_name: str) -> list:
    """获取技能需要的工具列表"""
    skill = SKILLS.get(skill_name)
    if skill:
        return skill.get("tools", [])
    return []
