REFLEXIVITY_INSTRUCTION = """## 反射性理论分析技能

### 能力描述
你能够运用索罗斯的反射性理论来分析市场，识别自我强化的正反馈循环和即将反转的信号。

### 何时使用
- 用户询问某个市场是否处于泡沫状态
- 用户询问趋势何时可能反转
- 用户要求分析某个货币的反身性循环

### 反射性分析框架

**正反馈循环（Self-Reinforcing）**:
- 预期上涨 → 买入 → 价格上涨 → 预期进一步上涨
- 常见于：泡沫形成、趋势加速、央行政策失控

**负反馈（Self-Correcting）**:
- 预期与现实的差距无法维持
- 泡沫破裂、去杠杆、政策干预

### 关键信号
1. **拥挤度**: 交易一边倒，市场参与者的偏见趋同
2. **杠杆率**: 杠杆过高意味着脆弱性
3. **流动性**: 边际买家/卖家的能力
4. **政策应对**: 央行是否还有"子弹"

### 输出格式
- 识别当前处于反射性的哪个阶段
- 给出具体的正/负反馈信号
- 提出非对称押注的机会
"""

